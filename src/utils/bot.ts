import { admins } from "@/vars/admins";
import { CommandContext, Context } from "grammy";

type StepFunctions = (ctx: CommandContext<Context>) => any;

export function adminsOnly(fn: StepFunctions, ctx: CommandContext<Context>) {
  const isAdmin = admins.some(
    ({ username }) => username === ctx.chat?.username
  );

  if (isAdmin) fn(ctx);
}

// eslint-disable-next-line
export function cleanUpBotMessage(text: any) {
  text = String(text);
  text = text
    .replace(/\./g, "\\.")
    .replace(/-/g, "\\-")
    .replace(/!/g, "\\!")
    .replace(/#/g, "\\#");

  return text;
}

// eslint-disable-next-line
export function hardCleanUpBotMessage(text: any) {
  text = String(text);
  text = text
    .replace(/\./g, "\\.")
    .replace(/-/g, "\\-")
    .replace(/_/g, "\\_")
    .replace(/\|/g, "\\|")
    .replace(/\(/g, "\\(")
    .replace(/\)/g, "\\)")
    .replace(/`/g, "\\`")
    .replace(/\+/g, "\\+")
    .replace(/!/g, "\\!")
    .replace(/#/g, "\\#")
    .replace(/\*/g, "\\*");

  return text;
}
